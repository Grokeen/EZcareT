FUNCTION DAMO.DEC_SSN2_PEN( inData IN VARCHAR2 )
RETURN VARCHAR2  PARALLEL_ENABLE DETERMINISTIC
Is
   DEC_RETURN             VARCHAR2(200) ;
BEGIN

   DEC_RETURN := DAMO.DEC_VARCHAR_PE('HBIL','APPATBAT','SSN2',inData);

   RETURN DEC_RETURN;

EXCEPTION
      WHEN OTHERS THEN
         RETURN 'CHECK ENCRYPT DATA OR DECRYPT PRIVILEGE!!';
END DEC_SSN2_PEN; 
